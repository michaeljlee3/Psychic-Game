var wins = 0;
var losses = 0;
var guessesLeft = 10;
var guessesSoFar = []; // array to push user choices to
var myString = guessesSoFar.join(', ')
var computerChoices = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']; //list of letters to choose from

document.onkeyup = function(event) {
var userGuess = event.key // user guess by pressing down key
var computerGuess = computerChoices[Math.floor(Math.random() * computerChoices.length) + 1]; // computer guess generated by multiplying math.random by 27
 // add user guess to guessesSoFar array

if (guessesSoFar.includes(userGuess)) {
    alert("You already chose this letter")
} else {guessesSoFar.push(userGuess)}

if (userGuess == computerGuess) {
    wins++; // increment wins by 1
    alert("You Win! " + "The correct letter was: " + userGuess); // alert that user won and display correct answer
    guessesLeft = 10; // reset after win
    guessesSoFar.length = 0;   
}

else if (guessesLeft === 0) {
    losses++; // increment losses by 1
    alert("You Lose!");
    guessesLeft = 10; // reset after win
    guessesSoFar.length = 0;
}

else if (userGuess != computerGuess) {
    guessesLeft--; // decrement guesses by 1
}

document.getElementById('wins').innerHTML = "Wins: " + wins;
document.getElementById('losses').innerHTML = "Losses: " + losses;
document.getElementById('guessesLeft').innerHTML = "Guesses Left: " + guessesLeft;
document.getElementById('guessesSoFar').innerHTML = "Guesses So Far: " + guessesSoFar;
};